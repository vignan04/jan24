package Crt;

public class StringSwapping {

	public static void main(String[] args) {
		String name1="Harsha";
		String name2="Vishnu";
		System.out.println("Before Swwapping....");
		System.out.println("name1...."+name1);
		System.out.println("name2...."+name2);
		
		String t;
		t=name1;
		name1=name2;
		name2=t;
		
		System.out.println("After Swwapping....");
		System.out.println("name1...."+name1);
		System.out.println("name2...."+name2);
		

		
	}

}
